    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String codigo = this.jTextField1.getText();
        int disciplina = this.jComboBox1.getSelectedIndex();
        int professor = this.jComboBox2.getSelectedIndex();
        int qtdVagas = Integer.parseInt(this.jTextField2.getText());
        int[] selecionados = this.jList1.getSelectedIndices();
        ArrayList<Aluno> alunos = new ArrayList<Aluno>();
        
        for(int i = 0; i < selecionados.length; i++){
            alunos.add(this.academico.getAlunos().get(selecionados[i]));
        }
        String semestre = this.jTextField3.getText();
        
        Turma t = new Turma(codigo, disciplina, professor, qtdVagas, alunos, semestre);
        this.academico.getTurmas().add(t);
        
    }//GEN-LAST:event_jButton2ActionPerformed

